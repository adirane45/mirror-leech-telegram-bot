#!/bin/bash
set -e

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

echo -e "${GREEN}????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????${NC}"
echo -e "${GREEN}  MLTB Containerized Deployment${NC}"
echo -e "${GREEN}  Phase 1 & 2 Complete${NC}"
echo -e "${GREEN}????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????${NC}"
echo

# Check Docker
if ! command -v docker &> /dev/null; then
    echo -e "${RED}??? Docker not found. Install: https://docs.docker.com/engine/install/${NC}"
    exit 1
fi

# Check Docker Compose
if ! docker compose version &> /dev/null; then
    echo -e "${RED}??? Docker Compose V2 not found.${NC}"
    exit 1
fi

echo -e "${GREEN}???${NC} Docker $(docker --version | cut -d' ' -f3)"
echo -e "${GREEN}???${NC} Docker Compose $(docker compose version --short)"
echo

# Stop old stack
echo -e "${YELLOW}Stopping old services...${NC}"
docker compose down 2>/dev/null || true
echo

# Build and start
echo -e "${YELLOW}Building images...${NC}"
docker compose build --no-cache
echo

echo -e "${YELLOW}Starting all services...${NC}"
docker compose up -d
echo

# Wait for health checks
echo -e "${YELLOW}Waiting for services (30s)...${NC}"
sleep 30

# Show status
echo
echo -e "${GREEN}????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????${NC}"
echo -e "${GREEN}  Service Status${NC}"
echo -e "${GREEN}????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????${NC}"
docker compose ps
echo

# Access info
echo -e "${GREEN}????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????${NC}"
echo -e "${GREEN}  Access Information${NC}"
echo -e "${GREEN}????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????${NC}"
echo -e "  ???? Bot: Active on Telegram"
echo -e "  ???? Dashboard: ${YELLOW}http://localhost:8060${NC}"
echo -e "  ???? Grafana: ${YELLOW}http://localhost:3000${NC} (admin/mltbadmin)"
echo -e "  ???? aria2: ${YELLOW}http://localhost:6800${NC}"
echo -e "  ???? qBittorrent: ${YELLOW}http://localhost:8090${NC} (admin/mltbmltb)"
echo -e "  ???? Prometheus: ${YELLOW}http://localhost:9091${NC}"
echo

# qBittorrent note
echo -e "${YELLOW}??????  First-time qBittorrent setup:${NC}"
echo -e "   Get temp password: ${YELLOW}docker logs mltb-qbittorrent 2>&1 | grep 'temporary password'${NC}"
echo -e "   Login ??? Change password to: ${YELLOW}mltbmltb${NC}"
echo

# Commands
echo -e "${GREEN}????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????${NC}"
echo -e "${GREEN}  Useful Commands${NC}"
echo -e "${GREEN}????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????${NC}"
echo -e "  Logs: ${YELLOW}docker compose logs -f${NC}"
echo -e "  Stop: ${YELLOW}docker compose down${NC}"
echo -e "  Restart: ${YELLOW}docker compose restart${NC}"
echo

echo -e "${GREEN}??? Deployment complete!${NC}"
echo -e "See ${YELLOW}DEPLOYMENT.md${NC} for full documentation."
